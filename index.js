// 1 & 2) Divide function expression
const divide = function() {
    return 2000 / 100;
};

// 3 & 4) Square arrow function
const square = (x) => x * x;

// 5 & 6) Add arrow function
const add = (a, b) => a + b;

// Example usage
console.log(divide()); // 20
console.log(square(5)); // 25
console.log(add(10, 20)); // 30
